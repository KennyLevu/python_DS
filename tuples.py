# Tuples are immutable lists
print("\n Tuples are immutable lists")

# Defining tuple with strings
tuple1 = ('Hello', 'this', 'is', 'kenny')
print("\n Defining tuple with strings'")
print(tuple1)

# Defining tuple with mixed types
tuple2 = ('Hello', 'I', 'am', 22)
print("\n Defining tuple with mixed types")
print(tuple2)

# Defining tuple with a list
list = ['Hello', 'I', 'am', 22]
print("\n Defined list")
print(list)
tuple3 = (list)
print("\n Defining tuple with a list")
print(tuple3)

# Accessing element from beginning
print("\n Access beginning of tuple")
print(tuple1[0])

# Accessing element from end
print("\n Acess end of tuple")
print(tuple1[-1])
