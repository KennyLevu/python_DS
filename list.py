# Creating a list
List = [1, "can", "be", "happy"]
print("\nList containing multiple values: ")
print(List)

# Creating a multi-dimensional list
List2 = [['Geeks', 2], ['Kenny']]
print("\nmulti-Dimensional List: ")
print(List2)

# indexing elements
print("Accessing elements by index")
print(List[0])
print(List[1])
print(List[2])
print(List2[1][0])

# accessing with negative index
print(List[-1])
print(List2[-1][0])

